#!/usr/bin/env bash

blue '
+-------------------------------------------------------------------------------
:  Visual Studio Code - Extensions - Other
+ - - - - - - - - - - - - - - - - - - - -
'

# Aural Coding (Keyboard sounds)
# A Visual Studio Code extension that creates sweet melodies based on what you type
# https://marketplace.visualstudio.com/items?itemName=jeng.aural-coding-vscode
code --install-extension jeng.aural-coding-vscode

# Code Runner
# Run code snippet or code file for multiple languages
# https://marketplace.visualstudio.com/items?itemName=formulahendry.code-runner
code --install-extension formulahendry.code-runner

# Code Spell Checker
# Spelling checker for source code
# https://marketplace.visualstudio.com/items?itemName=streetsidesoftware.code-spell-checker
code --install-extension streetsidesoftware.code-spell-checker

# Dash
# Dash, Zeal and Velocity integration in Visual Studio Code
# https://marketplace.visualstudio.com/items?itemName=deerawan.vscode-dash
code --install-extension deerawan.vscode-dash

# DevSkim
# Detect security mistakes as you type, and get real time details and fix suggestions
# https://marketplace.visualstudio.com/items?itemName=MS-DevSkim.vscode-devskim
code --install-extension MS-DevSkim.vscode-devskim

# Name That Color
# Get a friendly name from a Hex color representation.
# https://marketplace.visualstudio.com/items?itemName=guillaumedoutriaux.name-that-color
code --install-extension guillaumedoutriaux.name-that-color

# Partial Diff
# Compare (diff) text selections within a file, across files, or to the clipboard
# https://marketplace.visualstudio.com/items?itemName=ryu1kn.partial-diff
code --install-extension ryu1kn.partial-diff

# Path Intellisense
# Visual Studio Code plugin that autocompletes filenames
# https://marketplace.visualstudio.com/items?itemName=christian-kohler.path-intellisense
code --install-extension christian-kohler.path-intellisense

# Settings Cycler
# Allows cycling through vscode settings using keyboard shortcuts
# https://marketplace.visualstudio.com/items?itemName=hoovercj.vscode-settings-cycler
code --install-extension hoovercj.vscode-settings-cycler

# Settings Sync
# Synchronize Settings, Snippets, Themes, File Icons, Launch, Keybindings, Workspaces and Extensions Across Multiple Machines Using GitHub Gist.
# https://marketplace.visualstudio.com/items?itemName=Shan.code-settings-sync
code --install-extension Shan.code-settings-sync

# Sublime Text Keymap and Settings Importer
# Import Sublime Text settings and keybindings into VS Code.
# https://marketplace.visualstudio.com/items?itemName=ms-vscode.sublime-keybindings
code --install-extension ms-vscode.sublime-keybindings

# Live Share
# Real-time collaborative development from the comfort of your favorite tools.
# https://marketplace.visualstudio.com/items?itemName=ms-vsliveshare.vsliveshare
code --install-extension ms-vsliveshare.vsliveshare
