#!/usr/bin/env bash

blue '
+-------------------------------------------------------------------------------
:  Visual Studio Code - Extensions - JSON
+ - - - - - - - - - - - - - - - - - - - -
'

# JSON Organizer
# Help you organize JSON
# https://marketplace.visualstudio.com/items?itemName=rintoj.json-organizer
code --install-extension rintoj.json-organizer

# json2yaml
# Convert JSON to YAML, and YAML to JSON.
# https://marketplace.visualstudio.com/items?itemName=tuxtina.json2yaml
code --install-extension tuxtina.json2yaml

# Prettify JSON
# Visual Studio Code Prettify JSON Extension
# https://marketplace.visualstudio.com/items?itemName=mohsen1.prettify-json
code --install-extension mohsen1.prettify-json
