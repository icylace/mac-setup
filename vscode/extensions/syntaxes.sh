#!/usr/bin/env bash

blue '
+-------------------------------------------------------------------------------
:  Visual Studio Code - Extensions - Syntaxes
+ - - - - - - - - - - - - - - - - - - - -
'

# Apache Conf
# Syntax highlighter for Apache configuration files
# https://marketplace.visualstudio.com/items?itemName=mrmlnc.vscode-apache
code --install-extension mrmlnc.vscode-apache

# AsciiDoc
# Provides rich language support for AsciiDoc.
# https://marketplace.visualstudio.com/items?itemName=joaompinto.asciidoctor-vscode
code --install-extension joaompinto.asciidoctor-vscode

# AutoHotkey
# AutoHotkey language support for VS Code
# https://marketplace.visualstudio.com/items?itemName=slevesque.vscode-autohotkey
code --install-extension slevesque.vscode-autohotkey

# EditorConfig for VS Code
# EditorConfig Support for Visual Studio Code
# https://marketplace.visualstudio.com/items?itemName=EditorConfig.EditorConfig
code --install-extension EditorConfig.EditorConfig

# GraphQL for VSCode
# GraphQL syntax highlighting, linting, auto-complete, and more!
# https://marketplace.visualstudio.com/items?itemName=kumar-harsh.graphql-for-vscode
code --install-extension kumar-harsh.graphql-for-vscode

# Idris
# Idris Language Support for Visual Studio Code
# https://marketplace.visualstudio.com/items?itemName=zjhmale.Idris
code --install-extension zjhmale.Idris

# Log File Highlighter
# Adds color highlighting to log files to make it easier to follow the flow of log events and identify problems.
# https://marketplace.visualstudio.com/items?itemName=emilast.LogFileHighlighter
code --install-extension emilast.LogFileHighlighter

# PlantUML
# Rich PlantUML support for Visual Studio Code.
# https://marketplace.visualstudio.com/items?itemName=jebbs.plantuml
code --install-extension jebbs.plantuml
